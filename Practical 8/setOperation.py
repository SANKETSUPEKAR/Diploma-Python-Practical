s1 = {1, 2, 3, 4, 5}
s2 = {4, 5, 6, 7, 8}
print('Before Operation \n Set 1= {0} \n Set 2= {1}'.format(s1, s2))
print('After Union Operation = ', s1 | s2)
print('After Intersection Operation = ', s1 & s2)
print('After Differance Operation = ', s1 - s2)
print('After Symmetric Differance Operation = ', s1 ^ s2)
print('After Clear Operation = ', s1.clear())
